// Prefix and Postfix Operators.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>
using namespace std;

int main()
{
    //Prefix = increement then assing
    int s = 0;
    cout << ++s << endl; //1
    cout << s << endl; //1

    // Postfix = assign then increement 

    int y = 0;
    cout << y++ << endl; // 0
    cout << y; //1

    // There is a decreement too with -- Operator

}

